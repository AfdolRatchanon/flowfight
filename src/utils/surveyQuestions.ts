/** แปลค่าเฉลี่ยเป็นระดับความพึงพอใจ */
export function interpretSatisfactionScore(mean: number): string {
  if (mean >= 4.51) return 'มากที่สุด';
  if (mean >= 3.51) return 'มาก';
  if (mean >= 2.51) return 'ปานกลาง';
  if (mean >= 1.51) return 'น้อย';
  return 'น้อยที่สุด';
}
